#include "bignum.h"
/*==================================================================
 * 함수명 : init_input
 * 함수 설명 : 입력으로 들어온 변수들에 값을 지정
 * 입력 : 연산에 이용되기 위하여 초기화될 변수 2개 in1,in2
 * 출력 : none
==================================================================*/
void init_input(D_BINT_t in)
{
	in->sig = (rand() % 3) - 1;
	in->len = (rand() % 11) + 1;

	for (int i = in->len - 1; i >= 0; i--)
		in->dat[i] = rand_gen();
}